    private void login() {
        String UsuSenha = "";
        for (char c : txtUsuSenha.getPassword()) 
            UsuSenha += c;
        Usuario usr = Usuario.login(txtUsuLogin.getText(), UsuSenha);
        if (usr == null) {
            JOptionPane.showMessageDialog(this, "Usu치rio ou senha inv치lidos!", "Acesso restrito", JOptionPane.WARNING_MESSAGE);
        } else {
            MainMenu mm = new MainMenu();
            mm.setVisible(true);
            this.dispose();
        }
    }
    
