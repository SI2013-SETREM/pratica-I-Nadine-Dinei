    public void list() {
        DefaultTableModel dados = (DefaultTableModel) tblPessoa.getModel();
        dados.setNumRows(0);
        for (Pessoa p : Pessoa.listBusca()) {
            dados.addRow(new String[]{String.valueOf(p.getPesCodigo()), p.getPesNome(), p.getPesCPFCNPJ()});
        }
    }

